class Tile {
    constructor(tileNo, x, y) {
        this.width = 40
        this.height = 40
        this.x = x
        this.y = y
        this.image = Array.from(document.querySelectorAll('.tile'))[tileNo-1]
    }

    draw(context) {
        context.drawImage(this.image, this.x, this.y, this.width, this.height)
    }
}

export class Foreground {
    constructor() {
        this.tiles = this.getTiles()
        this.objects = []
    }

    getTiles() {
        let tiles = []

        tileMatrix.forEach((row,i)=>{
            row.forEach((tileNo,j)=>{
                if(tileNo != 0){
                    tiles.push(new Tile(tileNo, j*40, i*40))
                }
            })
        })

        return tiles
    }

    draw(context) {
        this.tiles.forEach(tile => tile.draw(context))
    }
}